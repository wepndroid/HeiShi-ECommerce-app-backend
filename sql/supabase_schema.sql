CREATE TABLE IF NOT EXISTS users (
    id              VARCHAR(36) PRIMARY KEY,
    nickname        VARCHAR(50) NOT NULL,
    -- Nullable: OAuth (Google/Apple/WeChat) users have no phone at sign-up.
    phone           VARCHAR(20) UNIQUE,
    email           VARCHAR(255),
    password_hash   VARCHAR(255) NOT NULL,
    avatar_url      VARCHAR(500),
    bio             TEXT,
    city            VARCHAR(100),
    language        VARCHAR(5) NOT NULL DEFAULT 'en',
    heishi_id       VARCHAR(20) NOT NULL UNIQUE,
    phone_verified  BOOLEAN NOT NULL DEFAULT TRUE,
    wechat_bound    BOOLEAN NOT NULL DEFAULT FALSE,
    alipay_bound    BOOLEAN NOT NULL DEFAULT FALSE,
    identity_verified BOOLEAN NOT NULL DEFAULT FALSE,
    business_verified BOOLEAN NOT NULL DEFAULT FALSE,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS ix_users_phone ON users (phone);
CREATE INDEX IF NOT EXISTS ix_users_email ON users (email);

CREATE TABLE IF NOT EXISTS refresh_tokens (
    id          VARCHAR(36) PRIMARY KEY,
    user_id     VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    token_hash  VARCHAR(255) NOT NULL UNIQUE,
    expires_at  TIMESTAMPTZ NOT NULL,
    revoked     BOOLEAN NOT NULL DEFAULT FALSE,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS ix_refresh_tokens_user_id ON refresh_tokens (user_id);

CREATE TABLE IF NOT EXISTS listings (
    id                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    seller_id           VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    type                VARCHAR(20) NOT NULL DEFAULT 'product',
    title               VARCHAR(200) NOT NULL,
    title_zh            VARCHAR(200),
    description         TEXT NOT NULL DEFAULT '',
    description_zh      TEXT,
    price               DOUBLE PRECISION NOT NULL,
    category_key        VARCHAR(50) NOT NULL,
    tag_key             VARCHAR(50) NOT NULL DEFAULT '',
    condition_key       VARCHAR(50),
    location_label      VARCHAR(100) NOT NULL,
    region_state        VARCHAR(10) NOT NULL DEFAULT 'VIC',
    region_city         VARCHAR(50) NOT NULL DEFAULT 'Melbourne',
    region_area         VARCHAR(50) NOT NULL DEFAULT 'Clayton',
    image_url           VARCHAR(500) NOT NULL,
    images_json         TEXT NOT NULL DEFAULT '[]',
    status              VARCHAR(20) NOT NULL DEFAULT 'active',
    negotiable          BOOLEAN NOT NULL DEFAULT FALSE,
    escrow_supported    BOOLEAN NOT NULL DEFAULT TRUE,
    pickup_methods_json TEXT NOT NULL DEFAULT '["meetup"]',
    service_icon        VARCHAR(30),
    view_count          INTEGER NOT NULL DEFAULT 0,
    favorite_count      INTEGER NOT NULL DEFAULT 0,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS ix_listings_seller_id ON listings (seller_id);
CREATE INDEX IF NOT EXISTS ix_listings_status ON listings (status);

CREATE TABLE IF NOT EXISTS orders (
    id                INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    buyer_id          VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    listing_id        INTEGER NOT NULL REFERENCES listings(id) ON DELETE CASCADE,
    seller_id         VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    status            VARCHAR(20) NOT NULL DEFAULT 'pendingPay',
    amount            DOUBLE PRECISION NOT NULL,
    escrow_fee        DOUBLE PRECISION NOT NULL DEFAULT 0,
    delivery_method   VARCHAR(50) NOT NULL DEFAULT 'meetup',
    payment_method_id VARCHAR(36),
    payment_method    VARCHAR(30),
    psp               VARCHAR(20),
    payment_status    VARCHAR(30),
    psp_payment_id    VARCHAR(100),
    psp_transaction_id VARCHAR(100),
    charge_currency   VARCHAR(3) NOT NULL DEFAULT 'aud',
    amount_minor      INTEGER,
    display_amount_cny DOUBLE PRECISION,
    payout_paused     BOOLEAN NOT NULL DEFAULT FALSE,
    payout_status     VARCHAR(30) NOT NULL DEFAULT 'pending',
    payout_provider   VARCHAR(20),
    payout_method_id  VARCHAR(36),
    payout_reference  VARCHAR(100),
    payout_failure_code VARCHAR(50),
    payout_failure_reason TEXT,
    payout_released_at TIMESTAMPTZ,
    payout_failed_at  TIMESTAMPTZ,
    payout_reversed_at TIMESTAMPTZ,
    payout_reversal_reference VARCHAR(100),
    is_abnormal       BOOLEAN NOT NULL DEFAULT FALSE,
    admin_notes       TEXT,
    dispute_status    VARCHAR(30),
    dispute_reason    TEXT,
    dispute_evidence_json TEXT NOT NULL DEFAULT '[]',
    confirmed_at      TIMESTAMPTZ,
    auto_confirm_at   TIMESTAMPTZ,
    created_at        TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at        TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS ix_orders_buyer_id ON orders (buyer_id);
CREATE INDEX IF NOT EXISTS ix_orders_listing_id ON orders (listing_id);
CREATE INDEX IF NOT EXISTS ix_orders_status ON orders (status);

