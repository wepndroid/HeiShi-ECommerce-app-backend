CREATE TABLE IF NOT EXISTS safety_reports (
    id          VARCHAR(36) PRIMARY KEY,
    reporter_id VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    target_type VARCHAR(20) NOT NULL,
    target_id   VARCHAR(50) NOT NULL,
    reason      VARCHAR(100) NOT NULL,
    details     TEXT,
    status      VARCHAR(20) NOT NULL DEFAULT 'pending',
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS ix_safety_reports_reporter_id ON safety_reports (reporter_id);

CREATE TABLE IF NOT EXISTS blocklist (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    blocker_id  VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    blocked_id  VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (blocker_id, blocked_id)
);

CREATE INDEX IF NOT EXISTS ix_blocklist_blocker_id ON blocklist (blocker_id);
CREATE INDEX IF NOT EXISTS ix_blocklist_blocked_id ON blocklist (blocked_id);

CREATE TABLE IF NOT EXISTS system_notifications (
    id         VARCHAR(36) PRIMARY KEY,
    user_id    VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    title       VARCHAR(200) NOT NULL,
    body        TEXT NOT NULL,
    unread      BOOLEAN NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS ix_system_notifications_user_id ON system_notifications (user_id);

CREATE TABLE IF NOT EXISTS reviews (
    id          VARCHAR(36) PRIMARY KEY,
    order_id    INTEGER NOT NULL REFERENCES orders(id) ON DELETE CASCADE,
    reviewer_id VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    rating      INTEGER NOT NULL,
    comment     TEXT,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (order_id, reviewer_id)
);

