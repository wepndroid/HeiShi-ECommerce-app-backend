CREATE TABLE IF NOT EXISTS favorites (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    listing_id  INTEGER NOT NULL REFERENCES listings(id) ON DELETE CASCADE,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (user_id, listing_id)
);

CREATE INDEX IF NOT EXISTS ix_favorites_user_id ON favorites (user_id);
CREATE INDEX IF NOT EXISTS ix_favorites_listing_id ON favorites (listing_id);

CREATE TABLE IF NOT EXISTS view_history (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    listing_id  INTEGER NOT NULL REFERENCES listings(id) ON DELETE CASCADE,
    viewed_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (user_id, listing_id)
);

CREATE INDEX IF NOT EXISTS ix_view_history_user_id ON view_history (user_id);
CREATE INDEX IF NOT EXISTS ix_view_history_listing_id ON view_history (listing_id);

CREATE TABLE IF NOT EXISTS follows (
    id           INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    follower_id  VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    followed_id  VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    created_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (follower_id, followed_id)
);

CREATE INDEX IF NOT EXISTS ix_follows_follower_id ON follows (follower_id);
CREATE INDEX IF NOT EXISTS ix_follows_followed_id ON follows (followed_id);

CREATE TABLE IF NOT EXISTS coupons (
    id          VARCHAR(36) PRIMARY KEY,
    user_id     VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    amount      DOUBLE PRECISION NOT NULL,
    description VARCHAR(200) NOT NULL,
    kind        VARCHAR(30),
    expires_at  TIMESTAMPTZ,
    status      VARCHAR(20) NOT NULL DEFAULT 'available'
);

CREATE INDEX IF NOT EXISTS ix_coupons_user_id ON coupons (user_id);

-- ============================================================
-- 3. Messaging
-- ============================================================

CREATE TABLE IF NOT EXISTS conversations (
    id                VARCHAR(36) PRIMARY KEY,
    listing_id        INTEGER NOT NULL REFERENCES listings(id) ON DELETE CASCADE,
    buyer_id          VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    seller_id         VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    last_message_text TEXT,
    last_message_at   TIMESTAMPTZ,
    buyer_unread      INTEGER NOT NULL DEFAULT 0,
    seller_unread     INTEGER NOT NULL DEFAULT 0,
    buyer_read_inbox_at  TIMESTAMPTZ,
    seller_read_inbox_at TIMESTAMPTZ,
    buyer_marked_unread  BOOLEAN NOT NULL DEFAULT FALSE,
    seller_marked_unread BOOLEAN NOT NULL DEFAULT FALSE,
    created_at        TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS ix_conversations_listing_id ON conversations (listing_id);
CREATE INDEX IF NOT EXISTS ix_conversations_buyer_id ON conversations (buyer_id);
CREATE INDEX IF NOT EXISTS ix_conversations_seller_id ON conversations (seller_id);

CREATE TABLE IF NOT EXISTS messages (
    id              VARCHAR(36) PRIMARY KEY,
    conversation_id VARCHAR(36) NOT NULL REFERENCES conversations(id) ON DELETE CASCADE,
    sender_id       VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    text            TEXT NOT NULL,
    sent_at         TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS ix_messages_conversation_id ON messages (conversation_id);

