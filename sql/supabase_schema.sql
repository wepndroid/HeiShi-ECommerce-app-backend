CREATE TABLE IF NOT EXISTS addresses (
    id           VARCHAR(36) PRIMARY KEY,
    user_id      VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    label        VARCHAR(100) NOT NULL,
    area         VARCHAR(100) NOT NULL,
    meetup_spot  VARCHAR(200),
    is_default   BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE INDEX IF NOT EXISTS ix_addresses_user_id ON addresses (user_id);

CREATE TABLE IF NOT EXISTS payment_methods (
    id                        VARCHAR(36) PRIMARY KEY,
    user_id                   VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    type                      VARCHAR(20) NOT NULL,
    label                     VARCHAR(100) NOT NULL,
    last4                     VARCHAR(4),
    is_default                BOOLEAN NOT NULL DEFAULT FALSE,
    stripe_payment_method_id  VARCHAR(100),
    brand                     VARCHAR(20),
    exp_month                 INTEGER,
    exp_year                  INTEGER
);

CREATE INDEX IF NOT EXISTS ix_payment_methods_user_id ON payment_methods (user_id);

CREATE TABLE IF NOT EXISTS payout_methods (
    id                          VARCHAR(36) PRIMARY KEY,
    user_id                     VARCHAR(36) NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    type                        VARCHAR(20) NOT NULL,
    label                       VARCHAR(100) NOT NULL,
    last4                       VARCHAR(4),
    account_ref                 VARCHAR(255),
    is_default                  BOOLEAN NOT NULL DEFAULT FALSE,
    stripe_external_account_id  VARCHAR(100),
    payouts_enabled             BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE INDEX IF NOT EXISTS ix_payout_methods_user_id ON payout_methods (user_id);

CREATE TABLE IF NOT EXISTS user_settings (
    user_id           VARCHAR(36) PRIMARY KEY REFERENCES users(id) ON DELETE CASCADE,
    intent_alerts     BOOLEAN NOT NULL DEFAULT TRUE,
    chat_messages     BOOLEAN NOT NULL DEFAULT TRUE,
    review_results    BOOLEAN NOT NULL DEFAULT TRUE,
    marketing         BOOLEAN NOT NULL DEFAULT FALSE,
    find_by_phone     BOOLEAN NOT NULL DEFAULT TRUE,
    show_wechat_badge BOOLEAN NOT NULL DEFAULT FALSE,
    personalization   BOOLEAN NOT NULL DEFAULT TRUE
);

